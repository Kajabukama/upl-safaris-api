DROP DATABASE IF EXISTS `apes_db`;
CREATE DATABASE `apes_db`;
USE `apes_db`;

DROP TABLE IF EXISTS `users`;
CREATE TABLE `users` (
	id  INT(11)NOT NULL AUTO_INCREMENT PRIMARY KEY,
	token VARCHAR(100)NOT NULL,
	firstname VARCHAR(255)NOT NULL,
	lastname VARCHAR(255)NOT NULL,
	email VARCHAR(255)NOT NULL,
	mobile VARCHAR(255)NOT NULL,
	password VARCHAR(255)NOT NULL,
	active TINYINT NOT NULL default 0,
	avatar VARCHAR(255)NOT NULL default 'default.jpg',
	created_at TIMESTAMP NOT NULL,
	updated_at TIMESTAMP NOT NULL
) AUTO_INCREMENT = 1000;

DROP TABLE IF EXISTS `tbl_csv`;
CREATE TABLE `tbl_csv`(
	id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	uid INT NOT NULL,
	sid VARCHAR(100) NOT NULL,
	indexno VARCHAR(100) NOT NULL,
	student_name VARCHAR(100) NOT NULL,
	uploaded_on TIMESTAMP NOT NULL
) AUTO_INCREMENT = 1000;

DROP TABLE IF EXISTS `tbl_photos`;
CREATE TABLE `tbl_photos`(
	id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	uid INT NOT NULL,
	sid VARCHAR(100) NULL,
	indexno VARCHAR(100) NOT NULL,
	student_name VARCHAR(100) NOT NULL,
	thumb VARCHAR(100) NOT NULL,
	uploaded_on TIMESTAMP NOT NULL
) AUTO_INCREMENT = 1000;

